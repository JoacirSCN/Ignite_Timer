import { HandPalm, Play } from 'phosphor-react'
import { useForm, FormProvider } from 'react-hook-form'
import { zodResolver } from '@hookform/resolvers/zod'
import * as zod from 'zod'
import { useContext } from 'react'

import {
  HomeContainer,
  StartCountdownButton,
  StopCountdownButton,
} from './styles'

import { NewCycleForm } from './components/NewCycleForm'
import { Countdown } from './components/Countdown'
import { CycleContext } from '../../contexts/CyclesContext'

// Controlled -> Quando mantemos em tempo real as informações dos inputs do usuário guardadas no estado da nossa aplicação

// Uncontrolled -> É ideal pra quando se tem MUITOS inputs na tela, utilizar liby react hook form
